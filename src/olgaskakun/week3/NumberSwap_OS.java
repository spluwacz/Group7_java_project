package olgaskakun.week3;

public class NumberSwap_OS {
    public static void main(String[] args) {

        int a = 5; //first variable
        int b = 10; //second variable

        System.out.println("Original Value:a= "+a + ", b= " + b+".");
        System.out.println("-----------------------------------");

        a = a + b; //5+10=15
        b = a - b; //15-10=5
        a = a - b; //15-5=10

        System.out.println("New Value:a= "+ a+ " , b= "+b+".");









    }
}
